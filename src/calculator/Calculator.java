package calculator;

public class Calculator {
	
	int add(int i, int j) { //
		
		return i + j ;
	}
	// GNGJNGJNGJNGJNGJNGJGNJGNJGNGJNGG
	int subtract(int i, int j) { // 
		
		return i - j;
	}
	
	int multiply(int i, int j){ // 
		
		return i * j;
	}
	
	int divide(int i, int j) { // 
		
		return i / j;
	}
	
	
}
